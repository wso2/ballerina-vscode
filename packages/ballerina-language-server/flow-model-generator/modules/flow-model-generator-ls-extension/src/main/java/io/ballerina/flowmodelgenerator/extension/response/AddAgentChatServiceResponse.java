package io.ballerina.flowmodelgenerator.extension.response;

/**
 * Response for the add agent chat service request. Contains the endpoint path, position of the service, and whether the
 * service already existed.
 *
 * @since 1.7.0
 */
public class AddAgentChatServiceResponse extends AbstractFlowModelResponse {

    private String chatEndpointPath;
    private boolean alreadyExists;
    private String filePath;
    private int startLine;
    private int startColumn;
    private int endLine;
    private int endColumn;

    public AddAgentChatServiceResponse() {
    }

    public String getChatEndpointPath() {
        return chatEndpointPath;
    }

    public void setChatEndpointPath(String chatEndpointPath) {
        this.chatEndpointPath = chatEndpointPath;
    }

    public boolean isAlreadyExists() {
        return alreadyExists;
    }

    public void setAlreadyExists(boolean alreadyExists) {
        this.alreadyExists = alreadyExists;
    }

    public String getFilePath() {
        return filePath;
    }

    public void setFilePath(String filePath) {
        this.filePath = filePath;
    }

    public int getStartLine() {
        return startLine;
    }

    public void setStartLine(int startLine) {
        this.startLine = startLine;
    }

    public int getStartColumn() {
        return startColumn;
    }

    public void setStartColumn(int startColumn) {
        this.startColumn = startColumn;
    }

    public int getEndLine() {
        return endLine;
    }

    public void setEndLine(int endLine) {
        this.endLine = endLine;
    }

    public int getEndColumn() {
        return endColumn;
    }

    public void setEndColumn(int endColumn) {
        this.endColumn = endColumn;
    }
}
